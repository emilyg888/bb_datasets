"""End-to-end fraud detection pipeline runner.

Loads from DuckDB → builds features → runs rule detector → reports
precision / recall against the ground-truth `fraud_flag` label.
"""

from __future__ import annotations

import sys
from pathlib import Path

# Make `fraud` and `datasets` importable when run as `python fraud/run_fraud.py`
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from fraud.detector import detect_fraud  # noqa: E402
from fraud.features import build_features  # noqa: E402
from fraud.load import load_transactions  # noqa: E402


def _confusion_counts(df) -> dict[str, int]:
    """TP / FP / FN / TN against ground-truth `fraud_flag`."""
    truth = df["fraud_flag"].astype(bool)
    pred  = df["predicted_fraud"].astype(bool)
    return {
        "tp": int(( truth &  pred).sum()),
        "fp": int((~truth &  pred).sum()),
        "fn": int(( truth & ~pred).sum()),
        "tn": int((~truth & ~pred).sum()),
    }


def _metrics(c: dict[str, int]) -> dict[str, float]:
    precision = c["tp"] / (c["tp"] + c["fp"]) if (c["tp"] + c["fp"]) else 0.0
    recall    = c["tp"] / (c["tp"] + c["fn"]) if (c["tp"] + c["fn"]) else 0.0
    f1        = 2 * precision * recall / (precision + recall) if (precision + recall) else 0.0
    return {"precision": precision, "recall": recall, "f1": f1}


def run() -> int:
    print("Loading transactions from DuckDB...")
    df = load_transactions()
    print(f"  loaded {len(df):,} rows")

    print("Building features...")
    df = build_features(df)

    print("Running rule-based detector...")
    df = detect_fraud(df)

    n_pred  = int(df["predicted_fraud"].sum())
    n_truth = int(df["fraud_flag"].sum()) if "fraud_flag" in df.columns else 0
    print(f"  predicted fraud: {n_pred:,} ({n_pred/len(df)*100:.2f}%)")
    print(f"  ground truth   : {n_truth:,} ({n_truth/len(df)*100:.2f}%)")

    if "fraud_flag" not in df.columns:
        print("\n(no fraud_flag column — dataset wasn't generated by v3, skipping metrics)")
        return 0

    print("\n--- evaluation against ground truth ---")
    c = _confusion_counts(df)
    print(f"  TP={c['tp']:>5}  FP={c['fp']:>5}")
    print(f"  FN={c['fn']:>5}  TN={c['tn']:>5}")

    m = _metrics(c)
    print(f"\n  precision = {m['precision']:.3f}")
    print(f"  recall    = {m['recall']:.3f}")
    print(f"  f1        = {m['f1']:.3f}")

    print("\n--- rule contribution (predicted=True) ---")
    print(f"  is_spike    : {int(df['is_spike'].sum()):>5}")
    print(f"  is_velocity : {int(df['is_velocity'].sum()):>5}")
    print(f"  is_anomaly  : {int(df['is_anomaly'].sum()):>5}")

    return 0


if __name__ == "__main__":
    raise SystemExit(run())
